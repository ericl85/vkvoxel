    void VkRenderer::cleanup() {
        VkDevice device = _manager->getDevice();
        VmaAllocator allocator = _manager->getAllocator();

        cleanupSwapChain();

        // Cleanup uniform buffers
        for (size_t i = 0; i < swapChainImages.size(); i++) {
            vmaDestroyBuffer(allocator, uniformBuffers[i], uniformBufferAllocations[i]);
        }

        // Cleanup our chunk
        chunk->cleanup();

        // Cleanup sync objects
        for (size_t i = 0; i < MAX_FRAMES_IN_FLIGHT; i++) {
            vkDestroySemaphore(device, renderFinishedSemaphores[i], nullptr);
            vkDestroySemaphore(device, imageAvailableSemaphores[i], nullptr);
            vkDestroyFence(device, inFlightFences[i], nullptr);
        }

        _manager->cleanup();
        vkDestroySurfaceKHR(_manager->getInstance(), surface, nullptr);
        _manager->cleanupInstance();
    }

    void VkRenderer::cleanupSwapChain() {
        VkDevice device = _manager->getDevice();
        VkCommandPool commandPool = _manager->getCommandPool();

        for (auto framebuffer : swapChainFramebuffers) {
            vkDestroyFramebuffer(device, framebuffer, nullptr);
        }

        vkFreeCommandBuffers(device, commandPool, static_cast<uint32_t>(commandBuffers.size()), commandBuffers.data());

        vkDestroyPipeline(device, graphicsPipeline, nullptr);
        vkDestroyPipelineLayout(device, pipelineLayout, nullptr);
        vkDestroyRenderPass(device, renderPass, nullptr);

        for (auto imageView : swapChainImageViews) {
            vkDestroyImageView(device, imageView, nullptr);
        }

        vkDestroySwapchainKHR(device, swapChain, nullptr);
    }

    void VkRenderer::initialize() {
        #ifdef NDEBUG
        const bool enableValidationLayers = false;
        #else
        const bool enableValidationLayers = true;
        #endif

        // Setup the Vulkan instance
        _manager = std::make_shared<VkManager>();
        _manager->setupInstance(enableValidationLayers);
        
        // Create our window surface
        createSurface(_manager->getInstance());

        // Now setup the vulkan device
        _manager->setupDevice(surface);

        createSwapChain();
        createImageViews();
        createRenderPass();
        createDescriptorSetLayout();
        createGraphicsPipeline();
        createFramebuffers();

        blockTypes.resize(2);
        blockTypes[1].id = 1;
        blockTypes[1].vertexes = BLOCK_VERTICES;
        createChunk();

        createUniformBuffers();
        createDescriptorPool();
        createDescriptorSets();
        createCommandBuffers();
        createSyncObjects();

        framebufferResized = false;
    }

